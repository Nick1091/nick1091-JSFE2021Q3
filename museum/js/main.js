$(document).ready(function() {
    $('.burger').click(function(event) {
        $('.burger, .navigation').toggleClass('active');
    });  
});

$(document).ready(function(){
    $("#navigation").on("click","a", function (event) {
        event.preventDefault();
        var id  = $(this).attr('href'),
            top = $(id).offset().top;
        $('body,html').animate({scrollTop: top}, 1000);
    });
});

